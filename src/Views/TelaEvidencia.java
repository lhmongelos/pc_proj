    private void BotaoOcorrenciasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotaoOcorrenciasMouseClicked
        // TODO add your handling code here:
        ArrayList<Evidencia> oc = new ArrayList<Evidencia>();

        String s = idOcorrencia.getText();

        int chave;

        if(s == null || "".equals(s)){
            chave = 0;
        }
        else{
            chave = Integer.parseInt(idOcorrencia.getText());
        }

        try{
            oc = control.QueryEvidencias(chave);
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("id Evidencia");
            modelo.addColumn("tipo");
            modelo.addColumn("envolvimento");
            modelo.addColumn("providencia");
            jTable1.setModel(modelo);

            jTable1.getColumnModel().getColumn(0).setPreferredWidth(186);
            jTable1.getColumnModel().getColumn(1).setPreferredWidth(186);
            jTable1.getColumnModel().getColumn(2).setPreferredWidth(186);
            jTable1.getColumnModel().getColumn(3).setPreferredWidth(186);

            int j = 0;
            for(Evidencia o: oc){
                modelo.setNumRows(j + 1);
                jTable1.setValueAt(o.getIdEvidencia(), j, 0);
                jTable1.setValueAt(o.getTipo(), j, 1);
                jTable1.setValueAt(o.getEnvolvimento(), j, 2);
                jTable1.setValueAt(o.getProvidencia(), j, 3);
                j++;
            }

        }
        catch (SQLException ex) {
            Logger.getLogger(TelaOcorrencia.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BotaoOcorrenciasMouseClicked

