    private void BotaoCadastrarEvidenciaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotaoCadastrarEvidenciaMouseClicked
        // TODO add your handling code here:
        int idOcorrenc = Integer.parseInt(idOcorrenciaAssoc.getText());
        Evidencia ev = new Evidencia();
        
        ev.setIdEvidencia(Integer.parseInt(idEvidencia.getText()));
        ev.setEnvolvimento(envolvimento.getText());
        ev.setTipo(tipo.getText());
        ev.setProvidencia(provimento.getText());
        
        
        try {
            control.adicionarEvidencia(idOcorrenc, ev);
        } catch (SQLException ex) {
            Logger.getLogger(TelaEvidencia.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_BotaoCadastrarEvidenciaMouseClicked

