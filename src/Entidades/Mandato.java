public class Mandato {
    
    private String nomeAutor;

    private int idOcorrencia;

    /**
     * @return the nomeAutor
     */
    public String getNomeAutor() {
        return nomeAutor;
    }

    /**
     * @param nomeAutor the nomeAutor to set
     */
    public void setNomeAutor(String nomeAutor) {
        this.nomeAutor = nomeAutor;
    }

    /**
     * @return the nomeOcorrencia
     */
    public int getidOcorrencia() {
        return idOcorrencia;
    }

    /**
     * @param nomeOcorrencia the nomeOcorrencia to set
     */
    public void setidOcorrencia(int nomeOcorrencia) {
        this.idOcorrencia = nomeOcorrencia;
    }

}
